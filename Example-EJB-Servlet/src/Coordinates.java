public class Coordinates{
	private int id;
	private float lat;
	private float lon;
	public Coordinates(int id, float lat, float lon){
		this.id=id;
		this.lat=lat;
		this.lon=lon;
	}
	public int getId(){
		return this.id;
	}
	public float getLat(){
		return this.lat;
	}
	public float getLon(){
		return this.lon;
	}
}